<template>
  <button :class="classes" :disabled="disabled" type="button" @click="onClick">
    <slot />
  </button>
</template>

<script>
export default {
  name: "KbnButton",

  props: {
    type: {
      type: String,
      default: "button",
    },
    disabled: {
      type: Boolean,
      default: false,
    },
  },

  computed: {
    classes() {
      const cls = this.type === "text" ? "-" + this.type : "";
      return [`kbn-button${cls}`];
    },
  },

  methods: {
    onClick(evt) {
      this.$emit("click", evt);
    },
  },
};
</script>

<style scoped>
.kbn-button {
  padding: 0.6em 1.3em;
}

.kbn-button-text {
  border: none;
  padding-right: 0;
  padding-left: 0;
}
</style>
